public class For_example {
    public static void main(String[] args) 
    {
        for (int i=0;i<=7;i++)
        {
            System.out.println("DAY "+i);

            for(int j=9;j<18;j++)
            {
                System.out.println(j+"am"+"-"+(j+1)+"am");
            }
        }
    }
    
}
